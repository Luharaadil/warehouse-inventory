export function t(lang: Language, key: keyof typeof translations['zh'], params?: Record<string, string | number>) {
  let text = translations[lang][key] || translations['zh'][key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}
